/**
 * Class representing the keyboard input.
 * Captures the state of pressed keys for character control.
 */
class Keyboard {
    /**
     * Indicates if the left arrow key is pressed.
     * @type {boolean}
     */
    LEFT = false;
    /**
     * Indicates if the right arrow key is pressed.
     * @type {boolean}
     */
    RIGHT = false;
    /**
     * Indicates if the up arrow key is pressed.
     * @type {boolean}
     */
    UP = false;
    /**
     * Indicates if the down arrow key is pressed.
     * @type {boolean}
     */
    DOWN = false;
    /**
     * Indicates if the spacebar is pressed.
     * @type {boolean}
     */
    SPACE = false;
    /**
     * Indicates if the 'O' key is pressed (throw bottle).
     * @type {boolean}
     */
    THROW = false;
}